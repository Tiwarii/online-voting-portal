package com.ovp.utils;

import java.util.Random;

/**
 *
 * @author pjayswal
 */
public class RandomUtils {

    public static int getRandomNumber() {
        Random r = new Random(System.currentTimeMillis());
        return ((1 + r.nextInt(2)) * 10000 + r.nextInt(10000));
    }
}
